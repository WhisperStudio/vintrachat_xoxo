'use client'

import { useEffect, useState } from 'react'

export type VintraLanguage = 'no' | 'en'

export const VINTRA_LANGUAGE_STORAGE_KEY = 'vintra-language'
const LEGACY_LANGUAGE_STORAGE_KEY = 'vintra-main-language'
const LANGUAGE_CHANGE_EVENT = 'vintra-language-change'

export const languageLabels: Record<VintraLanguage, string> = {
  no: 'Norsk',
  en: 'English',
}

export const languageOptions: VintraLanguage[] = ['no', 'en']

export function detectPreferredLanguage(): VintraLanguage {
  if (typeof window === 'undefined') return 'en'

  const savedLanguage =
    window.localStorage.getItem(VINTRA_LANGUAGE_STORAGE_KEY) ||
    window.localStorage.getItem(LEGACY_LANGUAGE_STORAGE_KEY)

  if (savedLanguage === 'no' || savedLanguage === 'en') return savedLanguage

  const languages = window.navigator.languages?.length
    ? window.navigator.languages
    : [window.navigator.language]
  const usesNorwegian = languages.some((language) => {
    const lowered = language?.toLowerCase()

    return lowered?.startsWith('no') || lowered?.startsWith('nb') || lowered?.startsWith('nn')
  })
  const isInNorwayTimeZone =
    Intl.DateTimeFormat().resolvedOptions().timeZone === 'Europe/Oslo'

  return usesNorwegian || isInNorwayTimeZone ? 'no' : 'en'
}

export function setStoredLanguage(nextLanguage: VintraLanguage) {
  if (typeof window === 'undefined') return

  window.localStorage.setItem(VINTRA_LANGUAGE_STORAGE_KEY, nextLanguage)
  window.localStorage.setItem(LEGACY_LANGUAGE_STORAGE_KEY, nextLanguage)
  window.dispatchEvent(
    new CustomEvent(LANGUAGE_CHANGE_EVENT, {
      detail: nextLanguage,
    })
  )
}

export function useVintraLanguage() {
  const [language, setLanguageState] = useState<VintraLanguage>('en')

  useEffect(() => {
    setLanguageState(detectPreferredLanguage())

    const syncLanguage = () => setLanguageState(detectPreferredLanguage())
    const syncFromEvent = (event: Event) => {
      const nextLanguage = (event as CustomEvent<VintraLanguage>).detail

      if (nextLanguage === 'no' || nextLanguage === 'en') {
        setLanguageState(nextLanguage)
      }
    }

    window.addEventListener('storage', syncLanguage)
    window.addEventListener(LANGUAGE_CHANGE_EVENT, syncFromEvent)

    return () => {
      window.removeEventListener('storage', syncLanguage)
      window.removeEventListener(LANGUAGE_CHANGE_EVENT, syncFromEvent)
    }
  }, [])

  const setLanguage = (nextLanguage: VintraLanguage) => {
    setLanguageState(nextLanguage)
    setStoredLanguage(nextLanguage)
  }

  return { language, setLanguage }
}

